package com.Java_Practice;

// 3. Write a program for a Single line comment, multi-line and documentation comments
public class Commenting {

	public static void main(String[] args) {

		// This is a single line comments
		System.out.println("// This is a single comment ");

		// This is a Multiline comment
		System.out.println("/* This is a Multiline comment */");

		// This is documentation comment
		System.out.println("/** This is documentation comment */");

	}

}
