package com.Loops;

//1. Write a program to print “Bright IT Career” ten times using for loop
public class ToPrint_10_Times {

	public static void loops() {
             // (int a = 0; a < 10; a++)
		for (int a = 1; a <= 10; a++) {
			System.out.println(a + ". Bright IT Career");
		}
	}

	public static void main(String[] args) {
		loops();

	}

}
